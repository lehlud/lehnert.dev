    <h1>Point, Action, Result, Ask (PARA)</h1>


    <h2>What it is</h2>
    <p>The <b>P</b>oint, <b>A</b>ction, <b>R</b>esult, <b>A</b>sk method is a short, structured way to communicate clearly. It helps you present a situation, explain what you did, show the outcome, and close with a clear request or next step.</p>


    <h2>When to use it</h2>
    <p>Use PARA in interviews, performance reviews, status updates, sales conversations, or anytime you need to make a concise and persuasive statement.</p>


    <h2>Structure</h2>
    <table>
        <tbody>
            <tr>
                <th>Point</th>
                <td>Begin with the main point or context. State the situation, problem, or goal in one or two sentences.</td>
            </tr>
            <tr>
                <th>Action</th>
                <td>Describe the specific actions you took. Be clear about your role and the steps you implemented â€” focus on what you did rather than what the team did.</td>
            </tr>
            <tr>
                <th>Result</th>
                <td>Summarize the outcome and, when possible, include measurable impact (numbers, percentages, time saved, etc.). Keep this concise.</td>
            </tr>
            <tr>
                <th>Ask</th>
                <td>End with a clear ask: what do you want the listener to do next? This could be approval, feedback, resources, a follow-up meeting, or an invitation to collaborate.</td>
            </tr>
        </tbody>
    </table>


    <h2>Template</h2>
    <table>
        <tbody>
            <tr>
                <th>Point</th>
                <td>[situation/context: 1 sentence]</td>
            </tr>
            <tr>
                <th>Action</th>
                <td>[what you did: 1-2 sentences]</td>
            </tr>
            <tr>
                <th>Result</th>
                <td>[measurable outcome: 1 sentence]</td>
            </tr>
            <tr>
                <th>Ask</th>
                <td>[what you want next: 1 sentence]</td>
            </tr>
        </tbody>
    </table>

    <h2>Example</h2>
    <table>
        <tbody>
            <tr>
                <th>Point</th>
                <td>Customer satisfaction scores fell by 15% over three months.</td>
            </tr>
            <tr>
                <th>Action</th>
                <td>I analyzed feedback, identified slow response times as the main issue and implemented a ticket triage system with clear SLAs.</td>
            </tr>
            <tr>
                <th>Result</th>
                <td>Response times improved by 40% and satisfaction returned to 92% within two months.</td>
            </tr>
            <tr>
                <th>Ask</th>
                <td>I would like to apply the same approach here, can we run a two month pilot phase on our support queue?</td>
            </tr>
        </tbody>
    </table>

    <h2>Tips</h2>
    <p>Keep each section short and specific. Use numbers when available. Tailor your ask to the audience and be prepared to provide next steps if they agree.</p>

